package com.koreait.first.var;

//변수타입 설명
public class Var1 {
	public static void main(String args[]) {
		//primitive 타입 (원시형 타입) / 일반변수라고 하기도 한다.
		//정수형, 실수형, 불린형, 문자형이 있다.
		
		//정수형
		byte d1;				//1byte	(8bit) -128~127  |	00000000
		short d2;				//2byte
		int d3;					//4byte		정수형은 주로 int를 쓴다
		long d4;				//8byte
		
		//실수형
		float f1;				//4byte
		double f2;				//8byte		쓰자
		
		//불린형
		boolean b1;				// true or false
		
		//문자형
		char c1;				// 문자를 1개만 쓸수있기때문에 거의 안씀
		
		
		
		//주석 > 프로그래밍에서 실행을 하지 않는 부분!
		//즉, 우리가 코멘트를 남기고 싶을 때
		//혹은 프로그램이 실행되지 않았으면 할 때 사용할 수 있음.
		
		// -> 싱글라인 주석
		
		/*
		 
		  멀티라인 주석
		 
		*/
		
	}
}
